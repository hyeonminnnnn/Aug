package kr.ac.kopo.aug.model;

public class Cate {
	private int cgId;
	private String name;
	private int boardId;

	public int getCgId() {
		return cgId;
	}

	public void setCgId(int cgId) {
		this.cgId = cgId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getBoardId() {
		return boardId;
	}

	public void setBoardId(int boardId) {
		this.boardId = boardId;
	}
}
